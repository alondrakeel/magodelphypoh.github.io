            <p>El pOH es una medida de la basicidad de una solución. Se calcula utilizando la fórmula:</p>
            <p><strong>pOH = -log[OH⁻]</strong></p>
            <p>Donde [OH⁻] es la concentración de iones hidróxido en moles por litro.</p>
            <p>El pOH es el logaritmo negativo en base a 10 de la actividad de los aniones hidróxilo. Tiene las mismas propiedades del pH en disolución acuosa de valor entre 0 y 14, pero en este caso las soluciones con pOH mayores a 7 son ácidas, y básicas o alcalinas las que tengan valores menores de 7.</p>
