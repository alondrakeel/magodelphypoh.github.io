        <h1>PH</h1>
        <div class="info-box">
            <p>El pH es una medida de la acidez o alcalinidad de una solución. Se calcula utilizando la fórmula:</p>
            <p><strong>pH = -log[H⁺]</strong></p>
            <p>Donde [H⁺] es la concentración de iones de hidrógeno en moles por litro.</p>
            <p>El pH es una medida que sirve para establecer el nivel de acidez o alcalinidad de una disolución. La “p” es por “potencial”, por eso el pH se llama: potencial de hidrógeno.</p>
            <p>Se expresa como el logaritmo negativo de base 10 de la concentración de iones hidrógeno.</p>
            <p>La escala de pH mide las soluciones ácidas (mayor concentración de iones de hidrógeno) y las alcalinas (base, de menor concentración) de las sustancias.</p>
            <p>La escala numérica va de 0 a 14, donde las sustancias más ácidas se acercan al número 0, y las más alcalinas (o básicas) se aproximan al número 14.</p>
            <p>Las sustancias ácidas como el jugo de limón tiene un pH entre 2 y 3 o la orina entre 4 y 7. Por su parte, los jugos gástricos tienen un valor entre 1 y 2 o los ácidos de baterías que se encuentran entre 1 y 0.</p>
            <p>Por el contrario, las sustancias alcalinas o base tiene valores más altos como la leche de magnesia entre 10 y 11 o los limpiadores con amoníaco cuyo valor está entre 11 y 12.</p>
            <p>También se pueden calcular las medidas del pH utilizando un potenciómetro o pH Metro, que es un sensor que determina el pH de una sustancia a través de una membrana de vidrio que separa dos soluciones de diferente concentración de protones.</p>
